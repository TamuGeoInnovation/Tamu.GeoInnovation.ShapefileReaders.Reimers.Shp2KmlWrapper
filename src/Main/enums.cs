namespace Reimers.Esri
{
    /// <summary>
    /// Enumeration of different supported shape types.
    /// </summary>
    public enum ShapeType
    {
        /// <summary>
        /// A Null shape.
        /// </summary>
        Null = 0,
        /// <summary>
        /// A Point shape.
        /// </summary>
        Point = 1,
        /// <summary>
        /// An Undefined shape with shape value 2
        /// </summary>
        Undefined2 = 2,
        /// <summary>
        /// A Polyline shape.
        /// </summary>
        PolyLine = 3,
        /// <summary>
        /// An Undefined shape with shape value 4
        /// </summary>
        Undefined4 = 4,
        /// <summary>
        /// A Polygon shape.
        /// </summary>
        Polygon = 5,
        /// <summary>
        /// An Undefined shape with shape value 6
        /// </summary>
        Undefined6 = 6,
        /// <summary>
        /// An Undefined shape with shape value 7
        /// </summary>
        Undefined7 = 7,
        /// <summary>
        /// A Multipoint shape.
        /// </summary>
        MultiPoint = 8
    }
}